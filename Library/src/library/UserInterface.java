	private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {
		String bookId = jTextField13.getText();
		String title = jTextField14.getText();
		String author = jTextField15.getText();
		String category = CategoryDropdown.getSelectedItem().toString();
		String language = LanguageDropdown.getSelectedItem().toString();
		boolean availableForLoan = availableFlag_1.isSelected();

		boolean valid;
		valid = getValidator().isValidBook(bookId, title, author, category, language, availableForLoan);

		if (valid) {
			try {
				Book book = new Book();
				book.setTitle(title);
				book.setAuthor(author);
				book.setAvailableForLoan(availableForLoan);
				book.setBookId(bookId);
				book.setCategory(category);
				book.setLanguage(language);

				String result = getBooks().insertBook(book);
				jTextArea4.setText(result);
				clearInsertBookFields();
			} catch (Exception ex) {
				Logger.getLogger(UserInterface.class.getName()).log(Level.SEVERE, null, ex);
			}
		} else {
			jTextArea4.setText("Please insert valid data");
		}
	}

